function arrow_click()
{
    var display_style = 
    {
        display:"block",
        position:"relative",
        right:"40px",
        transition:"2s",
    };
    var display_hide=
    {
        display:"none",
    }

    var service1=document.getElementById('service1');
    var service2=document.getElementById('service2');
    var service3=document.getElementById('service3'); 

    if(service1.style.display=='block')
    {
        $(".service1").css(display_hide);
        $(".service2").css(display_style);
        $(".service3").css(display_hide);
        return;
    }

    if(service2.style.display=='block')
    {
        $(".service1").css(display_hide);
        $(".service2").css(display_hide);
        $(".service3").css(display_style);
        return;
    }

    if(service3.style.display=='block')
    {
        $(".service1").css(display_style);
        $(".service2").css(display_hide);
        $(".service3").css(display_hide);
        return;
    }

   
}
